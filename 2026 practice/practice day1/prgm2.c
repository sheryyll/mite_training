// WAP to add a int with float numbers
#include<stdio.h>
int main(void) {
    float num1, num2, num3;

    printf("Enter first number: ");
    scanf("%f", &num1);

    printf("Enter second number: ");
    scanf("%f", &num2);

    num3 = num1 + num3;

    printf("Addtion of int with float numbers %.2f and %.2f: %d", num1, num2, (int)num3);

    return 0;
}